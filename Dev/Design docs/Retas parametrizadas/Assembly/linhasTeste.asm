BaseAdressEB: var #1
CurAdressEB: var #1



main:
	
	static PlayerX + #0, #15
	static PlayerY + #0, #15
	static PlayerV + #0, #4
	static BaseAdressEB + #0, #32768
	static CurAdressEB + #0, #32768

	loadn r0, #CurAdressEB
	loadn r1, #20
	;-- EXPANSÃO DE @SET r0, r1, r2, EBPos.x --
	loadi r2, r0
	push r1
	loadn r1, #65472
	and r2, r2, r1
	pop r1
	shiftl0 r1, #0
	or r2, r2, r1
	storei r0, r2
	;--- FIM DA EXPANSÃO ---
	loadn r1, #10
	;-- EXPANSÃO DE @SET r0, r1, r2, EBPos.y --
	loadi r2, r0
	push r1
	loadn r1, #61503
	and r2, r2, r1
	pop r1
	shiftl0 r1, #6
	or r2, r2, r1
	storei r0, r2
	;--- FIM DA EXPANSÃO ---
	loadn r1, #0
	;-- EXPANSÃO DE @SET r0, r1, r2, EBPos.t --
	loadi r2, r0
	push r1
	loadn r1, #4095
	and r2, r2, r1
	pop r1
	shiftl0 r1, #12
	or r2, r2, r1
	storei r0, r2
	;--- FIM DA EXPANSÃO ---
	inc r0
	loadn r1, #0 ; direita
	;-- EXPANSÃO DE @SET r0, r1, r2, EBPos.A --
	loadi r2, r0
	push r1
	loadn r1, #65408
	and r2, r2, r1
	pop r1
	shiftl0 r1, #0
	or r2, r2, r1
	storei r0, r2
	;--- FIM DA EXPANSÃO ---
	loadn r1, #1 ; 1 pixel por frame
	;-- EXPANSÃO DE @SET r0, r1, r2, EBPos.v --
	loadi r2, r0
	push r1
	loadn r1, #64639
	and r2, r2, r1
	pop r1
	shiftl0 r1, #7
	or r2, r2, r1
	storei r0, r2
	;--- FIM DA EXPANSÃO ---

	main_LOOP:

	call MOVE_EB
	call RENDER_EB

	load r1, #FrameMod

	loadn r2, #0
	cmp r2, r1
	;cne DELAY

	inc r1
	loadn r2, #5 ; IMPORTANTE, ISSO É O MOD DO FrameMod
	mod r1, r1, r2
	store #FrameMod, r1

	jmp main_LOOP




