DELAY:
	push r0
	push r1
	loadn r0, #2500
	loadn r1, #0

	DELAY_loop:
	dec r0
	cmp r1, r0
	jne DELAY_loop

	pop r1
	pop r0
	rts

;internos: r0, r1, r2
LIMPAR_TELA:
	push r0
	push r1
	push r2

	loadn r0, #0 ; comparação
	load r1, #FrameMod
	cmp r1, r0
	jne LIMPAR_TELA_loop_fim
	
	loadn r0, #0 ; posicao
	loadn r1, #1200 ; char
	loadn r2, #127

	LIMPAR_TELA_loop:
	cmp r0, r1
	jeg LIMPAR_TELA_loop_fim
	outchar r2, r0
	inc r0
	jmp LIMPAR_TELA_loop
	LIMPAR_TELA_loop_fim:

	pop r2
	pop r1
	pop r0
	rts

RENDER_EB:
	loadn r0, #CurAdressEB
	;--- EXPANSÃO DE @GET r2, r0, r1, EBPos.x ---
	loadi r2, r0
	loadn r1, #63
	and r2, r2, r1
	shiftr0 r2, #0
	;--- FIM DA EXPANSÃO ---
	;--- EXPANSÃO DE @GET r3, r0, r1, EBPos.y ---
	loadi r3, r0
	loadn r1, #4032
	and r3, r3, r1
	shiftr0 r3, #6
	;--- FIM DA EXPANSÃO ---
	loadn r1, #40
	mul r3, r3, r1 ; calcula y*40
	add r2, r2, r3 ; calcula x + y*40
	loadn r1, #'X'
	
	outchar r1, r2

	rts

MOVE_EB:
	; r5 = CurAdressEB
	; r4 = temp
	; r3 = A*16 + t
	; r2 = t
	; r0 = dx/dy/t_teto, leitura das tabelas
	; r1 = x/y

	loadn r5, #CurAdressEB

	;--- EXPANSÃO DE @GET r2, r5, r4 EBPos.t ---
	loadi r2, r5
	loadn r4, #61440
	and r2, r2, r4
	shiftr0 r2, #12
	;--- FIM DA EXPANSÃO ---
	inc r5
	;--- EXPANSÃO DE @GET r3, r5, r4 EBPos.A ---
	loadi r3, r5
	loadn r4, #127
	and r3, r3, r4
	shiftr0 r3, #0
	;--- FIM DA EXPANSÃO ---

	shiftl0 r3, #4 ; calcula A*16
	add r3, r3, r2 ; calcula A*16 + t, mantém t em r2


	loadn r0, #CINEMATICA_LINEAR_DX
	add r0, r0, r3
	loadi r0, r0 ; r0 contém dx

	dec r5
	;--- EXPANSÃO DE @GET r1, r5, r4, EBPos.x ---
	loadi r1, r5
	loadn r4, #63
	and r1, r1, r4
	shiftr0 r1, #0
	;--- FIM DA EXPANSÃO ---
	add r1, r1, r0
	;-- EXPANSÃO DE @SET r5, r1, r4, EBPos.x --
	loadi r4, r5
	push r1
	loadn r1, #65472
	and r4, r4, r1
	pop r1
	shiftl0 r1, #0
	or r4, r4, r1
	storei r5, r4
	;--- FIM DA EXPANSÃO ---


	loadn r0, #CINEMATICA_LINEAR_DY
	add r0, r0, r3
	loadi r0, r0 ; r0 contém dy

	;--- EXPANSÃO DE @GET r1, r5, r4, EBPos.y ---
	loadi r1, r5
	loadn r4, #4032
	and r1, r1, r4
	shiftr0 r1, #6
	;--- FIM DA EXPANSÃO ---
	add r1, r1, r0
	;-- EXPANSÃO DE @SET r5, r1, r4, EBPos.y --
	loadi r4, r5
	push r1
	loadn r1, #61503
	and r4, r4, r1
	pop r1
	shiftl0 r1, #6
	or r4, r4, r1
	storei r5, r4
	;--- FIM DA EXPANSÃO ---


	inc r2 ; t é incrementado
	loadn r0, #CINEMATICA_LINEAR_TETO
	add r0, r0, r3
	loadi r0, r0 ; r0 contém t_teto
	cmp r2, r0 ; comparar t com t_teto
	jne ARMAZENAR_t

	loadn r2, #0

	ARMAZENAR_t:
	;-- EXPANSÃO DE @SET r5, r2, r4, EBPos.t --
	loadi r4, r5
	push r2
	loadn r2, #4095
	and r4, r4, r2
	pop r2
	shiftl0 r2, #12
	or r4, r4, r2
	storei r5, r4
	;--- FIM DA EXPANSÃO ---

	rts

